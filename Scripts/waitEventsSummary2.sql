  AND sw.event != 'Streams AQ: deallocate messages from Streams Pool'
  AND sw.event != 'Streams AQ: delete acknowledged messages'
  AND sw.event != 'Streams AQ: qmn coordinator idle wait'
  AND sw.event != 'Streams AQ: qmn slave idle wait'
  AND sw.event != 'Streams AQ: waiting for messages in the queue'
  AND sw.event != 'Streams AQ: waiting for time management or cleanup tasks'
  AND sw.event != 'Streams capture: waiting for archive log'
  AND sw.event != 'Streams fetch slave: waiting for txns'
  AND sw.event != 'class slave wait'
  AND sw.event != 'client message'
  AND sw.event != 'cmon timer'
  AND sw.event != 'dispatcher timer'
  AND sw.event != 'fbar timer'
  AND sw.event != 'gcs for action'
  AND sw.event != 'gcs remote message'
  AND sw.event != 'ges remote message'
