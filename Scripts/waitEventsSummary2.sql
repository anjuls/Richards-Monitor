,      COUNT(*) 
FROM gv$session_wait sw 
WHERE sw.wait_time = 0 
  AND sw.inst_id = ? 
  AND sw.event != 'AQ Proxy Cleanup Wait'
  AND sw.event != 'ASM background timer'
  AND sw.event != 'DBRM Logical Idle Wait'
  AND sw.event != 'DIAG idle wait'
  AND sw.event != 'EMON idle wait'
