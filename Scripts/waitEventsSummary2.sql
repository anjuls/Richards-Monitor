  AND sw.event != 'PX Deq Credit: need buffer'
  AND sw.event != 'PX Deq Credit: send blkd'
  AND sw.event != 'PX Deq: Execute Reply'
  AND sw.event != 'PX Deq: Execution Msg'
  AND sw.event != 'PX Deq: Par Recov Execute'
  AND sw.event != 'PX Deq: Signal ACK'
  AND sw.event != 'PX Deq: Table Q Normal'
  AND sw.event != 'PX Deq: Table Q Sample'
