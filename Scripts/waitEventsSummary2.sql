  AND sw.event != 'PX Idle Wait'
  AND sw.event != 'Queue Monitor Shutdown Wait'
  AND sw.event != 'Queue Monitor Slave Wait'
  AND sw.event != 'Queue Monitor Wait'
  AND sw.event != 'SQL*Net message from client'
  AND sw.event != 'SQL*Net message to client'
  AND sw.event != 'SQL*Net more data from client'
  AND sw.event != 'STREAMS apply coord waiting for slave message'
  AND sw.event != 'STREAMS apply slave idle wait'
