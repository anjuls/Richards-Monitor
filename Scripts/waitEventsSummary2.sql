  AND sw.event != 'LNS ASYNC archive log'
  AND sw.event != 'LNS ASYNC dest activation'
  AND sw.event != 'LNS ASYNC end of log'
  AND sw.event != 'LogMiner: client waiting for transaction'
  AND sw.event != 'LogMiner: generic process sleep'
  AND sw.event != 'LogMiner: reader waiting for more redo'
  AND sw.event != 'LogMiner: slave waiting for activate message'
  AND sw.event != 'LogMiner: waiting for processes to soft detach'
  AND sw.event != 'LogMiner: wakeup event for builder'
  AND sw.event != 'LogMiner: wakeup event for preparer'
  AND sw.event != 'LogMiner: wakeup event for reader'
  AND sw.event != 'MRP redo arrival'
